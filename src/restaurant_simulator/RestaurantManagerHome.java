    public RestaurantManagerHome() {
        super("Restaurant Simulator (MM): Home Manager");
        initComponents(); 
        RestaurantBuilderDirector rbd = new RestaurantBuilderDirector();
        RestaurantConcreteBuilder rcb = new RestaurantConcreteBuilder();
        rest = rbd.buildRestaurant(rcb);
        lblRestaurantName.setText(rest.getName());
        
        btnMenu.addActionListener(
            ev -> {
                ManagerMenu menuFrame = new ManagerMenu(rest.getMenu());
                this.setVisible(false);
                menuFrame.setVisible(true);
            });
        
        btnOrderHistory.addActionListener(
            ev -> {
                ManagerOrderList managerOrderList = new ManagerOrderList(this.rest);
                this.setVisible(false);
                managerOrderList.setVisible(true);
            });
        
        btnClients.addActionListener(
            ev -> {
                ManagerUserList managerUserList = new ManagerUserList(this.rest);
                this.setVisible(false);
                managerUserList.setVisible(true);
            });
        
        btnCalendary.addActionListener(
            ev -> {
                ManagerTableList managerTableList = new ManagerTableList(rest);
                this.setVisible(false);
                managerTableList.setVisible(true);
                managerTableList.toFront();
            });
        
        btnSimulator.addActionListener(
            ev -> {
                Simulator simulator = new Simulator(rest.getOrders());
                simulator.setVisible(true);
                this.setVisible(false);
            });
        
        menuGeneralSettings.addActionListener(
            ev -> {
                RestaurantGeneralConfig rc = new RestaurantGeneralConfig(rest.getName());
                rc.setVisible(true);
                this.setVisible(false);
            });
        
        menuTableSettings.addActionListener(
            ev -> {
                RestaurantTableConfig rtc = new RestaurantTableConfig(rest.getTables());
                rtc.setVisible(true);
                this.setVisible(false);
            });
        
        this.setResizable(false);
        this.setLocationRelativeTo(null);
        setDefaultCloseOperation(JFrame.DISPOSE_ON_CLOSE);

    }

